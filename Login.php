<?php
session_start();
require 'db.php';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $email_or_username = $connection->real_escape_string($_POST['email_or_username']);
    $password = $_POST['password'];

    // Fetch user from the database
    $stmt = $connection->prepare("SELECT * FROM users WHERE email = ? OR username = ?");
    $stmt->bind_param("ss", $email_or_username, $email_or_username);
    $stmt->execute();
    $result = $stmt->get_result();
    $user = $result->fetch_assoc();

    // Verify password
    if ($user && password_verify($password, $user['password'])) {
        $_SESSION['user'] = $user['username']; // Log the user in
        header("Location: recipe_list.php"); // Redirect to recipe list
        exit();
    } else {
        echo "Error: Invalid login credentials.";
    }

    $stmt->close();
    $connection->close();
}
